                <p>3.  Help the students to retain their chosen program at the College of Engineering and Computer Studies,</p>
                <p>4.  Foster social interaction, collaboration and camaraderie about programming among student members,</p>
                <p>5.  Develop a high-level problem solving analysis and understanding that can help improve the programming skills and competency among its members, and</p>
